tuple_1 = (1, 2, 3, 4, 5)
tuple_2 = tuple("Hello")

res = list(tuple_1 + tuple_2)
print("Список из двух кортежей:", res)
res = (res, tuple_1[::-1], tuple_2[::-1])
print("1 список и 2 зеркальных кортежа:", res)
print("Три значения с кортежа:\n",
      "С первого:", res[0][2], "\n",
      "Со второго:", res[1][2], "\n",
      "С третьего:", res[2][2])
